# strip ()
curso = ' Curso de Python '
print (curso)
print (curso.strip())

#lower ()
curso = 'Curso de Python'
print (curso.lower())

# upper ()
curso = 'Curso de Python'
print (curso.upper())

# replace ()
curso = 'Curso de Python'
print(curso.replace('e','3'))

# split()
curso = 'Curso de Python'
print (curso.split())

# in ou not in
curso = 'Curso de Python'
x = 'de' in curso
z = 'de' not in curso
print (x)
print (z)

